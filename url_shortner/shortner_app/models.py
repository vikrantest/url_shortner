from django.db import models
from common.models import BaseModel

# Create your models here.


class LongUrlModel(BaseModel):
	slug = models.CharField(max_length=50,blank=False,null=True)
	long_url = models.CharField(max_length=50,blank=False,null=True)
	expiry_time = models.IntegerField(default=9999)

	class Meta:
		db_table = 'url_store_tab'
		
